package travel.dto;

public class KboardDTO {
	private int kboard_num;
	private String kboard_area;
	private String kboard_city;
	private String kboard_category;
	private String kboard_title;
	private String kboard_imgname;
	private String kboard_save_imgname;
	private String kboard_content;
	private String kboard_plus_content;
	private String kboard_addr;
	private String kboard_x;
	private String kboard_y;
	
	public KboardDTO(){}

	public KboardDTO(int kboard_num, String kboard_area, String kboard_city, String kboard_category,
			String kboard_title, String kboard_imgname, String kboard_save_imgname, String kboard_content,
			String kboard_plus_content, String kboard_addr, String kboard_x, String kboard_y) {
		super();
		this.kboard_num = kboard_num;
		this.kboard_area = kboard_area;
		this.kboard_city = kboard_city;
		this.kboard_category = kboard_category;
		this.kboard_title = kboard_title;
		this.kboard_imgname = kboard_imgname;
		this.kboard_save_imgname = kboard_save_imgname;
		this.kboard_content = kboard_content;
		this.kboard_plus_content = kboard_plus_content;
		this.kboard_addr = kboard_addr;
		this.kboard_x = kboard_x;
		this.kboard_y = kboard_y;
	}

	public int getKboard_num() {
		return kboard_num;
	}

	public void setKboard_num(int kboard_num) {
		this.kboard_num = kboard_num;
	}

	public String getKboard_area() {
		return kboard_area;
	}

	public void setKboard_area(String kboard_area) {
		this.kboard_area = kboard_area;
	}

	public String getKboard_city() {
		return kboard_city;
	}

	public void setKboard_city(String kboard_city) {
		this.kboard_city = kboard_city;
	}

	public String getKboard_category() {
		return kboard_category;
	}

	public void setKboard_category(String kboard_category) {
		this.kboard_category = kboard_category;
	}

	public String getKboard_title() {
		return kboard_title;
	}

	public void setKboard_title(String kboard_title) {
		this.kboard_title = kboard_title;
	}

	public String getKboard_imgname() {
		return kboard_imgname;
	}

	public void setKboard_imgname(String kboard_imgname) {
		this.kboard_imgname = kboard_imgname;
	}

	public String getKboard_save_imgname() {
		return kboard_save_imgname;
	}

	public void setKboard_save_imgname(String kboard_save_imgname) {
		this.kboard_save_imgname = kboard_save_imgname;
	}

	public String getKboard_content() {
		return kboard_content;
	}

	public void setKboard_content(String kboard_content) {
		this.kboard_content = kboard_content;
	}

	public String getKboard_plus_content() {
		return kboard_plus_content;
	}

	public void setKboard_plus_content(String kboard_plus_content) {
		this.kboard_plus_content = kboard_plus_content;
	}

	public String getKboard_addr() {
		return kboard_addr;
	}

	public void setKboard_addr(String kboard_addr) {
		this.kboard_addr = kboard_addr;
	}

	public String getKboard_x() {
		return kboard_x;
	}

	public void setKboard_x(String kboard_x) {
		this.kboard_x = kboard_x;
	}

	public String getKboard_y() {
		return kboard_y;
	}

	public void setKboard_y(String kboard_y) {
		this.kboard_y = kboard_y;
	}
	
}
	